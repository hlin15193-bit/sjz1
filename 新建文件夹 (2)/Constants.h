inline ECharacterTypeInfo FindEntityTypeInfo(std::string Fname)
{

    if (Fname.find("BP_Interact") != std::string::npos && Fname.find("false") != std::string::npos)
    {
        return { ECharacterType::Interactor,"容器" };
    }
    if (Fname.find("BP_Interact_Computer_C") != std::string::npos)
    {
        return { ECharacterType::Interactor,"电脑" };
    }
    if (Fname.find("BP_Interactor_Container_SafeBox_C") != std::string::npos)
    {
        return { ECharacterType::Interactor,"保险箱" };
    }
        

    auto it = ECharacterTypeInfoMap.find(Fname);
    if (it != ECharacterTypeInfoMap.end()) {
        return it->second;
    }
    
    if (Fname.find("CodedLock") != std::string::npos)
    {
        return { ECharacterType::Door,"门" };
    }
    if (Fname.find("BP_PlayerExit") != std::string::npos)
    {
        return { ECharacterType::Exit,"撤离点" };
    }
    if (Fname.find("BP_ExitTrigger") != std::string::npos)
    {
        return { ECharacterType::TriggerExit,"拉闸点" };
    }

    return { ECharacterType::Unknown,"未知" };
}

