{
    std::string name = "";

    switch (DeadBoxType)
    {
    case 0:name = "AI盒子"; break;
    case 1:
        name = "玩家盒子";;
        break;
    case 2:name = "AI盒子";
        break;
    case 3:name = "AI盒子";
        break;
    case 4: name = "精英AI盒子";
        break;
    case 5:name = "精英AI盒子";; break;
    case 6:name = "6盒子"; break;
    case 7:name = "7盒子"; break;
    default: name = "盒子"; break;
        break;
    }
    return name;
};
